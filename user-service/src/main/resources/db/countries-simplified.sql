-- Drop existing table if exists
DROP TABLE IF EXISTS public.countries CASCADE;

-- Create simplified countries table
CREATE TABLE public.countries (
    id bigint NOT NULL,
    name character varying(100) NOT NULL
);

-- Add primary key
ALTER TABLE public.countries ADD CONSTRAINT countries_pkey PRIMARY KEY (id);

-- Create sequence for auto-incrementing ID
CREATE SEQUENCE IF NOT EXISTS countries_id_seq
    START WITH 1
    INCREMENT BY 1
    NO MINVALUE
    NO MAXVALUE
    CACHE 1;

-- Set the sequence as default for id column
ALTER TABLE public.countries ALTER COLUMN id SET DEFAULT nextval('countries_id_seq');
ALTER SEQUENCE countries_id_seq OWNED BY public.countries.id;

